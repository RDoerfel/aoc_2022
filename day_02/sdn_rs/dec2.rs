use std::str;
use std::collections::HashMap;

#[derive(Copy, Clone, PartialEq)]
enum GameOption {
    Rock     = 1,
    Paper    = 2,
    Scissors = 3,

    // Laziness wins again, this should really be a different enum
    Win  = 8,
    Tie  = 16,
    Loss = 32,
}


fn get_score_part1(throw_self: &GameOption, throw_enemy: &GameOption) -> (u32, u32) {
    let s = *throw_self  as u32;
    let e = *throw_enemy as u32;
    if s == e {
        // Tie
        return (s + 3, e + 3);
    }
    else if (s - 1 == e) || (s + 2 == e) { // Poor-man's % 3
        // We won
        return (s + 6, e + 0);
    }
    else {
        // Enemy won
        return (s + 0, e + 6);
    }
}

fn get_score_part2(outcome: &GameOption, throw_enemy: &GameOption) -> (u32, u32) {
    let e = *throw_enemy as u32;
    if *outcome == GameOption::Tie {
        return (e + 3, e + 3);
    }
    else if *outcome == GameOption::Win {
        return (6 + (if e == 3 {1} else {e+1}), e + 0);
    }
    else {
        return ((if e == 1 {3} else {e-1}), e + 6);
    }
}

fn solve(input: &str) {
    let mut total_self  = 0u32;
    let mut total_enemy = 0u32;
    let mut map1 = HashMap::new();
    map1.insert("A", GameOption::Rock);
    map1.insert("B", GameOption::Paper);
    map1.insert("C", GameOption::Scissors);
    map1.insert("X", GameOption::Rock);
    map1.insert("Y", GameOption::Paper);
    map1.insert("Z", GameOption::Scissors);
    let mut map2 = HashMap::new();
    map2.insert("A", GameOption::Rock);
    map2.insert("B", GameOption::Paper);
    map2.insert("C", GameOption::Scissors);
    map2.insert("X", GameOption::Loss);
    map2.insert("Y", GameOption::Tie);
    map2.insert("Z", GameOption::Win);

    for line in input.split_terminator('\n') {
        if !line.is_empty() {
            let round = line.split_ascii_whitespace();
            let round_indv = round.collect::<Vec<_>>();
            let throw_self  = map2.get(round_indv[1]).unwrap();
            let throw_enemy = map2.get(round_indv[0]).unwrap();
            let (score_self, score_enemy) = get_score_part2(throw_self, throw_enemy);

//            println!("{}: {}, {}", line, score_self, score_enemy);
            total_self  += score_self;
            total_enemy += score_enemy;
        }
    }
    println!("{}, {}", total_self, total_enemy);
}


